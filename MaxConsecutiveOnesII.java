	/*
	 * Given a binary array, find the maximum number of consecutive 1s in this array
	 * if you can flip at most one 0.
	 * 
	 * 
	 * Input: [1,0,1,1,0] Output: 4 Explanation: Flip the first zero will get the
	 * the maximum number of consecutive 1s. After flipping, the maximum number of
	 * consecutive 1s is 4.
	 */

	public static int findMaxConsecutiveOnes(int[] nums) {

		/*
		 * Any 1/0 string with at least one trailing 0 can be seeing as repeating of a
		 * pattern: a ones followed by 1 zero. We denote such a pattern by p(a)
		 */

		/*
		 * 0011011100 can be seen as p(0) + p(0) + p(2) + p(3) + p(0). count a pattern
		 * at its trailing 0.
		 */

		/*
		 * each occurrence of 0 signals the end of such a pattern.
		 * 
		 * patternEnds: the length of pattern that ends no later than i. length: the
		 * length of the run of 1 if i is in one.0 otherwise
		 */

		/*
		 * each index i: if nums[i] is 0: calculate the length of the pattern that ends
		 * at nums[i] , and put it into pattern. Reset length because we are no longer
		 * in a run;
		 * 
		 * if nums[i] is 1: increment length which records run length, and try to update
		 * max with (pattern + length);
		 */

		int maxConsecutive = 0, pattern = 0, length = 0;
		System.out.println("===================================");
		for (int i = 0; i < nums.length; i++) {
			// if nums[i]=1
			length++;
			// if hits 0
			if (nums[i] == 0) {
				pattern = length;
				// reset the length
				length = 0;
			}

			maxConsecutive = Math.max(maxConsecutive, pattern + length);
			// calculate max value
			//pattern+length changes
			System.out.println(pattern + length);
			System.out.println("*****************");
			//stays the same
			System.out.println(maxConsecutive);
			System.out.println("---------------------------------");
		}
		System.out.println("+++++++++++++++++++++++++++++++");
		System.out.println(maxConsecutive);
		return maxConsecutive;

	}

